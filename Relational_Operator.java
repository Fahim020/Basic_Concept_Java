
public class Relational_Operator {
 
public static void main(String[] args) {
    
// Relational/ Comparsion operator 

int a = 20;
int b = 10;

System.out.println("true/false" + (a<b));
System.out.println("true/false" + (a>b));
System.out.println("true/false" + (a<=b));
System.out.println("true/false" + (a>=b));
System.out.println("true/false" + (a==b));
System.out.println("true/false" + (a!=b));






}




}
